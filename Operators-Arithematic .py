#Write a program to demonstrate all arithmetic operators.
a = 10
b = 20
addition = a + b
print(f"{a} + {b} = {addition}")
subtraction = a - b
print(f"{a} - {b} = {subtraction}")
multiplication = a * b
print(f"{a} * {b} = {multiplication}")
division = a / b
print(f"{a} / {b} = {division}")
floor_division = a // b
print(f"{a} // {b} = {floor_division}")
modulus = a % b
print(f"{a} % {b} = {modulus}")
exponentiation = a ** b
print(f"{a} ** {b} = {exponentiation}")