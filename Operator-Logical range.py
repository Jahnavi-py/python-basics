#Use logical operators to check if a number lies within a specific range.
number = 10
upper_bound = 10
lower_bound = 20
if number >= upper_bound and number <=lower_bound:
    print(f"Number - {number} is within the range between {upper_bound} and {lower_bound}.")
else:
    print(f"Number - {number} is not within the range between {upper_bound} and {lower_bound}.")